function HomePage() {
  const [homePageData, setHomePageData] = React.useState({
    loading: true,
    reposList: [],
  });

  React.useEffect(() => {
    setHomePageData({
      reposList: REPO_LIST,
      loading: false,
    });
    return;
    // TODO: Uncomment following once app is ready
    fetch(GITHUB_URL)
      .then(res => res.json())
      .then((result) => {
        setHomePageData({ reposList: result.items, loading: false });
      })
      .catch((error) => {
        setHomePageData({ ...homePageData, loading: false });
        throw error;
      });
  }, []);

  if (homePageData.loading) {
    return <div>Loading...</div>;
  }

  return (
    <PageLayout>
      <main>
        <input type="text" placeholder="Search by repository name" />
        <CardList repoList={homePageData.reposList} />
      </main>
      <style jsx>{`
        main {
          width: 90%;
          margin: 0 auto;
          margin-bottom: 3rem;
        }

        input {
          width: 100%;
          box-sizing: border-box;
          margin-bottom: 1rem;
          font-size: 1rem;
          padding: 0.5rem;
          border-radius: 0.25rem;
          border: 1px solid #d3d3d3;
        }
      `}</style>
    </PageLayout>
  );
}

